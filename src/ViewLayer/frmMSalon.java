        aula.setCanon(cbCanon.getSelectedItem().toString());
        aula.setPintarron (cbPintarron.getSelectedItem().toString());
        aula.setVentanas(cbVentanas.getSelectedItem().toString());

        if (aula.Update()) { 
            JOptionPane.showMessageDialog(null, "Aula actualizada correctamente!");
        }
        this.dispose();
