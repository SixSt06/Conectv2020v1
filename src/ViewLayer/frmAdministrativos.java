    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (jtAdministrativos.getSelectedRow() >= 0) {
            int idAdministrativo = (int) jtAdministrativos.getValueAt(jtAdministrativos.getSelectedRow(), 0);
            frmMAdministrativo obj = new frmMAdministrativo(idAdministrativo);
            obj.setTitle("Modificar Alumno");
            obj.setModal(true);
            obj.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecciona un registro");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (jtAdministrativos.getSelectedRow() >= 0) {
            int idAdministrativo = (int) jtAdministrativos.getValueAt(jtAdministrativos.getSelectedRow(), 0);
            int validacion = JOptionPane.showConfirmDialog(null, "Â¿Seguro que quieres eliminar el registro?");
            if (validacion == 0) {
                if (new Administrativo(idAdministrativo).Delete()) {
                    JOptionPane.showMessageDialog(rootPane, "Registro eliminado correctamente!");
                    jtAdministrativos.setModel(new Administrativo().GetAllModel());

                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un registro! ");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

